<?php

    // Variable
    /*
        Es un espacio de memoria reservado para algún dato
        o información
    */

    /* 
    Reglas para declarar variables
    - Comienzan con el signo $
    - No se inician con números
    - No se declaran variables repetidas
    - No llevan espacios ni caracteres epseciales
    - No se usan palabras reservadas  
    - Por convensión se usa la notación camelCase, o snack_case
    */

    // Php es de tipado debil, eso quiere decir que no declara con el tipo de datos

    // Variables numéricas
    $miVariable = 5;

    // Variables con punto flotante o decimal
    $miFlotante = 4.5;

    // Variables tipo string o cadena
    $miString1 = "Juan";
    $miString2 = 'Hola mundo';

    // Variables booleanas
    $miBoleano1 = true;
    $miBoleano2 = false;
    $miBoleano3 = 1;
    $miBoleano4 = 0;

?>