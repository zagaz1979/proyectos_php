<?php

    // Determinar el día de la semana según un número

    $dia = 2;

    if ($dia == 1) {
        echo "Hoy es Lunes";
    } else if ($dia == 2) {
        echo "Hoy es Martes";
    } else if ($dia == 3) {
        echo "Hoy es Miercoles";
    } else if ($dia == 4) {
        echo "Hoy es Jueves";
    } else if ($dia == 5) {
        echo "Hoy es Viernes";
    } else if ($dia == 6) {
        echo "Hoy es Sábado";
    } else if ($dia == 7) {
        echo "Hoy es Domingo";
    } else {
        echo "Dia incorrecto";
    }

?>